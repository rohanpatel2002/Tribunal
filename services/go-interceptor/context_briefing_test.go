// TestEnhancedBriefingGeneration tests briefing synthesis
func TestEnhancedBriefingGeneration(t *testing.T) {
	gen := NewEnhancedBriefingGenerator()
	if gen == nil {
		t.Fatal("expected BriefingGenerator to be created")
	}

	resp := &AnalyzeResponse{
		Repository:     "rohanpatel2002/tribunal",
		PRNumber:       42,
		Recommendation: "REVIEW_REQUIRED",
		TotalFiles:     5,
		AIGenerated:    2,
		Critical:       0,
		High:           1,
		Medium:         1,
		Low:            3,
		Files: []FileAnalysis{
			{
				Path:          "db/migration.go",
				AIScore:       0.85,
				IsAIGenerated: true,
				RiskLevel:     "HIGH",
				Summary:       "AI-generated database migration without idempotency checks",
				Signals: SignalBreakdown{
					Style:   0.8,
					Pattern: 0.9,
					Risk:    0.7,
				},
			},
		},
	}

	ctx := &RepositoryContext{
		ContextRelevanceScore: 0.8,
		ServiceDependencies: []ServiceDep{
			{ServiceName: "app", Dependency: "postgres", Type: "db", Critical: true},
		},
		Incidents: []IncidentPattern{
			{
				Title:       "Migration Deadlock",
				Severity:    "HIGH",
				Description: "Database migration locked tables",
				RootCause:   "Missing lock timeout",
			},
		},
	}

	briefing := gen.GenerateEnhancedBriefing(resp, ctx)

	if briefing == "" {
		t.Fatal("expected non-empty briefing")
	}

	// Verify briefing contains expected content
	if len(briefing) < 100 {
		t.Errorf("briefing seems too short: %d chars", len(briefing))
	}

	// Verify key sections
	requiredSections := []string{
		"TRIBUNAL Context Briefing",
		"Executive Summary",
		"PR #42",
		"REVIEW_REQUIRED",
	}

	for _, section := range requiredSections {
		if !contains(briefing, section) {
			t.Errorf("briefing missing required section: %q", section)
		}
	}
}

