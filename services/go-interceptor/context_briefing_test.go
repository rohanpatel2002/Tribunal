// TestParseDeploymentHistory tests parsing releases into deployment events
func TestParseDeploymentHistory(t *testing.T) {
	releases := []map[string]interface{}{
		{
			"tag_name":   "v1.0.0",
			"created_at": "2026-04-01T10:00:00Z",
			"body":       "Release 1.0.0 - 10 files changed",
		},
		{
			"tag_name":   "v1.0.1",
			"created_at": "2026-04-02T10:00:00Z",
			"body":       "Hotfix: critical incident resolved. 2 files changed.",
		},
	}

	history := parseDeploymentHistory(releases)

	if len(history) != 2 {
		t.Errorf("expected 2 deployment events, got %d", len(history))
	}

	if history[0].Version != "v1.0.0" {
		t.Errorf("expected first version to be v1.0.0, got %q", history[0].Version)
	}

	if history[1].Status != "partial_rollback" {
		t.Errorf("expected second deployment to be marked as partial_rollback, got %q", history[1].Status)
	}
}

